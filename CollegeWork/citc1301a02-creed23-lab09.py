import math

def main():


    x = 4.54
    y = 6.29
    z = -4.71
    i = 3
    j = -5

    print(f'Absolute Value of x =',abs(x))
    print(f'Absolute Value of y =',abs(y))
    print(f'Absolute Value of z =',abs(z))
    print(f'Absolute Value of i =',abs(i))
    print(f'Absolute Value of j =',abs(j))

    print(f'Rounded Value of x to 1 decimal place =',round(x,1))
    print(f'Rounded Value of y to 1 decimal place =',round(y,1))
    print(f'Rounded Value of z to 1 decimal place =',round(z,1))
    print(f'Rounded Value of i to 1 decimal place =',round(i,1))
    print(f'Rounded Value of j to 1 decimal place =',round(j,1))

    print(f'Ceiling Value of x =',math.ceil(x))
    print(f'Ceiling Value of y =',math.ceil(y))
    print(f'Ceiling Value of z =',math.ceil(z))
    print(f'Ceiling Value of i =',math.ceil(i))
    print(f'Ceiling Value of j =',math.ceil(j))

    print(f'Floor Value of x =',math.floor(x))
    print(f'Floor Value of y =',math.floor(y))
    print(f'Floor Value of z =',math.floor(z))
    print(f'Floor Value of i =',math.floor(i))
    print(f'Floor Value of j =',math.floor(j))









main()